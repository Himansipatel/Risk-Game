package com.risk.model.file;

/**
 * This is an Schema Class for Continent.
 * 
 * @author MayankJariwala
 * @version 0.0.1
 */
public class Continent {

	private String name;
	private int score;

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}

}
